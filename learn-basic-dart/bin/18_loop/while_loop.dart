void main() {
  /// While Loop
  /*
    While loop adalah versi perulangan yang lebih sederhana dibandingkan for loop.
    Di while loop hanya terdapat kondisi perulangan, tanpa ada init statement dan post statement.
  */

  var counter = 1;

  while (counter <= 10) {
    print('Perulangan ke-$counter');
    counter++;
  }
}
