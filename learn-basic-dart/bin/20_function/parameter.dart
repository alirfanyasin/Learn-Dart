void sayHello(String firstName, String lastName) {
  print('$firstName $lastName');
}

void main() {
  /// Function Parameter
  /*
    Kita bisa mengirim informasi ke function yang kita panggil.
    Untuk melakukan hal tersebut, kita perlu menambahkan parameter atau argument
    di function yang kita buat.
    Cara membuat parameter sama seperti cara membuat variabel.
    Parameter di tempatkan di dalam kurung () di deklarasi function.
    Parameter bisa lebih dari satu, jika lebih dari satu, harus di pisah menggunakan tanda koma.
    Ketika memanggil function, kita bisa sebut nama functionnya, lalu gunakan kurung (), jika terdapat
    parameter di dalam kurung () silahkan masukkan parameter sesuai dengan jumlah parameternya.
  */

  sayHello('Irfan', 'Yasin');
}
