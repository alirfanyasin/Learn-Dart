int sum(int number1, int number2) => number1 + number2;

void main() {
  /// Function Short Expression
  /*
    Dimana jika terdapat function yang hanya satu baris, kita bisa
    menyingkatnya secara sederhana.
    Untuk membuat function short expression, kita tidak butuh {} dan juga tidak butuh return.
  */

  print(sum(20, 30));
  print(sum(13, 12));
}
