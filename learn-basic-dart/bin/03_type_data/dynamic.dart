void main() {
  /// Dynamic
  /*
    Type data dynamic bisa menampung semua jenis type data
  */
  dynamic variabel = 100; // int
  print(variabel);

  variabel = 10.5; // double
  print(variabel);

  variabel = 'Irfan Yasin'; // string
  print(variabel);

  variabel = true; // boolean
  print(variabel);
}
