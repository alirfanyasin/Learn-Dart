void main() {
  /// Null
  /*
    Kata kunci null merupakan type data representasi data kosong.
    Secara default jika membuat variabel namun tidak mendeklarasikan value, maka datanya null
    Namun jika kita ingin membuat sebuah variabel bisa diisi dengan null, dengan menambahkan karakter ? (tanda tanya)
  */

  int number1; // error
  int? number2; // null

  int angka1 = null; // error
  int? angka2 = null; // null
}
