void main() {
  /// Operator Aritmatika
  /*
    +   : Penjumlahan
    -   : Pengurangan atau negatif
    *   : Perkalian
    /   : Pembagian, hasil double
    ~/  : Pembagian, hasil int
    %   : Modulus atau sisa bagi
  */

  num numb1 = 10, numb2 = 3;

  num pemjumlahan = numb1 + numb2;
  num pengurangan = numb1 - numb2;
  num perkalian = numb1 * numb2;
  num pembagian1 = numb1 / numb2;
  num pembagian2 = numb1 ~/ numb2;
  num modulus = numb1 % numb2;
}
