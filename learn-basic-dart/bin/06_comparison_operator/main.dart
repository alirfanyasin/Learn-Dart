void main() {
  /// Operator Perbandingan
  /*
    Operator perbandingan adalah operator yang hasilnya adalah boolean
    Operator perbandingan bisa digunakan di tipe data Number dan String

    ==    : Sama dengan
    !=    : Tidak sama dengan
    >     : Lebih dari
    <     : Kurang dari
    <=    : Lebih dari atau sama dengan
    >=    : Kurang dari atau sama dengan
  */

  print(10 == 10); // true
  print(10 != 10); // false
  print(10 > 10); // false
  print(10 < 10); // false
  print(10 <= 10); // true
  print(10 >= 10); // true

  print("Irfan" == "Irfan"); // true
  print("Irfan" != "Irfan"); // false
}
